                <p class="proj-parag">
                  A mechanical arm programed using Arduino which records the
                  movements of a hand and mimics it.
                  <br /><br />

                  The mechanical arm reads the inputs from the gyroscope sensor
                  and the flex sensor located on the user's hand. The gyroscope
                  returns the angular changes, and the flex sensor returns the
                  amount of bending of the finger. Then these values are written
                  to servo motors used to make the arm. The arm uses a RGB LED and
                  piezo speaker to help the user understand what is happening. The
                  arm has three modes: 1. Manual Mode: Mimics the movements of the
                  hand gestures at the real-time. 2. Learning Mode: Manual+Records
                  the results of the sensors to a data set. 3. Auto Mode: This
                  mode will mimics the values learnt (pre-setted) from the data
                  set. <br /><br />
